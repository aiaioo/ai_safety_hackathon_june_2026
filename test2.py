# Simple JSON mode LLM call helper function - will be used by the Evaluator
#def JSON_llm(user_prompt : str, schema : BaseModel, system_prompt : Optional[str] = None, is_tool_call : bool = True):
def JSON_llm(messages : List[str], poison : str, is_tool_call : bool = True):
    """ Run a language model with the given user prompt and system prompt, and return a structured JSON object. """
    try:
        extract = client.chat.completions.create(
            messages=messages,
            model=MODEL,
            #tools=tools if is_tool_call else None,
            #tool_choice="auto", # Default value; lets model call tool or talk
            temperature=0.7,
            max_tokens=4000,        
            #response_format={
            #    "type": "json_object",
            #    "schema": schema.model_json_schema(),
            #},
        )
        
        #response = json.loads(extract.choices[0].message.content)
        assistant_message = extract.choices[0].message

        if assistant_message.tool_calls:
            #print("TOOL_CALLS: There are "+str(len(assistant_message.tool_calls))+" tool calls!")
            messages.append(json.loads(assistant_message.model_dump_json()))
            for tool_call in assistant_message.tool_calls:
                # Check which function the model wants to run
                if tool_call.function.name == "do_security_check":
                    # Extract arguments generated by the model
                    arguments = json.loads(tool_call.function.arguments)
                    
                    # Execute your local Python function
                    tool_output = do_security_check(arguments, poison)
                    
                    # Append the tool's result to your conversation history
                    messages.append({
                        "role": "tool",
                        "tool_call_id": tool_call.id, # Must match the exact ID provided by the model
                        "name": tool_call.function.name,
                        "content": tool_output
                    })

        # Second API call with the tool results included
        final_response = client.chat.completions.create(
            messages=messages,
            model=MODEL,
            temperature=0.7,
            max_tokens=4000,        
        )

        assistant_message = final_response.choices[0].message

        response = assistant_message.content
        messages.append({"role": "assistant", "content": response})

        return response
        
    except ValidationError as e:
        raise ValueError(f"Schema validation failed: {str(e)}")


